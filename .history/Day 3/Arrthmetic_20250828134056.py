'''Create two arrays  1, 2, 3 and  4, 5, 6. Add them element-wise.
Hint: Use + or np.add()'''

import numpy as np 
arr1=np.array([1,2,3])
arr2=np.array([4,5,6])
print(np.add(arr1,arr2))


